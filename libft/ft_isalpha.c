#include "libft.h"

/*
** A~Z(65~90)'s 6th bit(32) is 0 -> OR with 32 makes it same as a~z(97~122)
*/
int	ft_isalpha(int c)
{
	return (((unsigned)c | 32) - 'a' < 26);
}
